/**
 * Prompt 模板（速览 / 详解 / 追问）。
 *
 * 设计原则：面向程序员，输出"具体"而非"抽象"。
 *   - 抽象（差）："这段代码用来统计 nums 中元素的出现情况"
 *   - 具体（好）："返回 Counter（dict 子类），key 是 nums 的元素，value 是出现次数"
 *
 * 速览回答："这是不是我要找的？" → 一句话 + 关键签名
 * 详解回答："那我怎么用？"     → 最小示例 + 参数表 + 易错点
 */

export interface PromptVars {
  code: string;
  language: string;
}

export function quickExplainPrompt({ code, language }: PromptVars): string {
  return `你是一个面向程序员的代码速读助手。程序员扫一眼一段代码，要立刻拿到两块信息：
1) 一句话：「是什么 + 能干嘛」—— 既要说出返回值/产物的具体形式，也要说出典型用途
2) 签名：完整的函数/语法签名 —— 含所有参数（必填+可选）、可选参数的默认值、返回类型

请严格按以下 Markdown 输出（一句话不超过 60 个汉字）：

**一句话：** <两段信息用顿号、分号或"，常用来 X"自然连接。先说"是什么"再说"作用"。
  好例子：
    - "返回 Counter（dict 子类），key=元素 value=出现次数；常用来统计列表/字符串中各元素的出现频率"
    - "生成长度为 L 的列表，元素全为 0；常用来初始化定长缓冲区或计数数组"
    - "原地按 id 字段降序排序 arr，常用来对对象列表按某属性排序"
  禁止：
    - 抽象动词"处理/操作/执行/使用/实现/进行/完成"
    - 只说"是什么"不说"用来干嘛"
    - 只说"用来 X"不说"返回/产生什么">

**签名：** \`<完整签名，硬性要求：
  - 列出该 API 的所有重要形参，即使代码里没显式传也要列
  - 可选参数用 \`name=默认值\` 形式标注，例如 \`reverse=False\`、\`iterable=None\`
  - 必填参数只写类型，例如 \`nums: list\`
  - 末尾必须有 \`-> 返回类型\`（无返回值写 \`-> None\`）
  - 如果代码是纯语法/赋值，写出该语法的形式签名，例如 \`list * int -> list\`、\`a if cond else b -> typeof(a|b)\`
  - 多 API 时只取最关键的一个\`>

例 1（输入 \`c = collections.Counter(nums)\`）：
  **一句话：** 返回 Counter（dict 子类），key=元素 value=出现次数；常用来统计列表/字符串中各元素的出现频率。
  **签名：** \`collections.Counter(iterable=None, /, **kwds) -> Counter\`

例 2（输入 \`arr = [0] * L\`）：
  **一句话：** 生成长度为 L 的列表，元素全为 0；常用来初始化定长缓冲区或计数数组。
  **签名：** \`list * int -> list  # 等价 [elem] * count\`

例 3（输入 \`arr.sort(key=lambda x: x.id, reverse=True)\`）：
  **一句话：** 原地按 id 字段降序排序 arr；常用来对对象列表按某属性排序而不复制。
  **签名：** \`list.sort(*, key=None, reverse=False) -> None\`

其他硬性要求：
- 输出只有两行（"一句话"和"签名"），不写标题、不写"以下是"、不重复粘贴原代码
- 直接以 \`**一句话：**\` 开头

代码（${language}）：
\`\`\`
${code}
\`\`\``;
}

export function detailedExplainPrompt({ code, language }: PromptVars): string {
  return `你是一个面向程序员的代码深读助手。用户已经知道这段代码"是什么"，现在想知道"怎么用、有哪些坑"。

请严格按以下 Markdown 输出，三个 ## 标题不可缺少、不可改名：

## 最小示例
\`\`\`${language}
<3-5 行可以直接复制运行的代码，含必要的 import 或上下文>
\`\`\`
**输出：**
\`\`\`
<上述代码运行的真实结果，不要写"输出类似..."这种含糊话；如果没有标准输出就写"无输出（或返回值见下）">
\`\`\`

## 参数
| 参数 | 类型 | 必填 | 默认 | 说明 |
|------|------|------|------|------|
| <参数名> | <类型> | <是/否> | <默认值或 -> | <一句话用途> |

（只列代码里实际涉及的 API/函数的参数；如果代码不涉及任何函数调用，把此表格替换为一行：**无函数参数（纯语法/赋值）**。）

## 注意
- <非显然的坑或边界情况，每条一行，1-3 条；常见类型：性能、边界值、就地修改、空输入、可变默认值等>

硬性要求：
- 示例代码必须能直接复制粘贴运行，不要写伪代码
- 输出块写真实结果，估算不准就写最简单的能确定的例子
- "注意"只写非显然的点，不写"要注意类型"、"要注意边界"这种废话
- 不要写额外的章节、总结、客套话

代码（${language}）：
\`\`\`
${code}
\`\`\``;
}

export function chatSystemPrompt({ code, language }: PromptVars): string {
  return `你是 WTF（What's This Function），一个面向程序员的代码答疑助手。
用户正在研究这段代码（${language}）：
\`\`\`
${code}
\`\`\`

回答风格：
- 直接给答案，不寒暄、不复述问题
- 用具体名词代替抽象动词（说"返回 dict"而不是"处理数据"）
- 涉及 API 时给出签名或最小示例
- 默认 150 字以内，复杂问题可以展开但保持紧凑
- 跑题问题礼貌引导回当前代码`;
}
